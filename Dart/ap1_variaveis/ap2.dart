void main() {
  // Definindo Variáveis
  const double nota1 = 7.0;
  const double nota2 = 8.0;
  const double nota3 = 5.3;
  const double nota4 = 9.7;

  // Calculo da Média
  final double mediaFinal = (nota1 + nota2 + nota3 + nota4)/4;

  // Imprimindo
  print("A média entre [ $nota1 , $nota2 , $nota3 , $nota4 ] = $mediaFinal");
}